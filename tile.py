class Tile():
    def __init__(self):
        self.value
        self.xCoord
        self.yCoord

    def getValue(self):
        return self.value

    def setValue(self, value):
        self.value = value

    def getXCoord(self):
        return self.xCoord

    def setXCoord(self, xCoord):
        self.xCoord = xCoord

    def getYCoord(self):
        return self.yCoord

    def setYCoord(self, yCoord):
        self.yCoord = yCoord